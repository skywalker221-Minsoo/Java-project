package com.mega.project;

public class MiniVO {
	
	private int mini_nm;
	private String mini_name;
	private String mini_content;
	private String mini_image;
	private int mini_price;

	public String getMini_name() {
		return mini_name;
	}

	public void setMini_name(String mini_name) {
		this.mini_name = mini_name;
	}

	public String getMini_content() {
		return mini_content;
	}

	public int getMini_nm() {
		return mini_nm;
	}

	public void setMini_nm(int mini_nm) {
		this.mini_nm = mini_nm;
	}

	public void setMini_content(String mini_content) {
		this.mini_content = mini_content;
	}

	public String getMini_image() {
		return mini_image;
	}

	public void setMini_image(String mini_image) {
		this.mini_image = mini_image;
	}

	public int getMini_price() {
		return mini_price;
	}

	public void setMini_price(int mini_price) {
		this.mini_price = mini_price;
	}

}
